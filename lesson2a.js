// javascript object data type
// an object data type is a data type that stores data in a form of key-value pair
let person ={
    name:"joseph william",
    age:"53",
    isteaccher:true
}

// below we print out the entire object
console.log("the details of the person are:",person)

// how to access specific values of an object
// we use the dot seperator to do this
console.log("the name of the person is:",person.name)
console.log("the age of the person is:",person.age)
console.log("is the person a teacher:",person.isteaccher)


